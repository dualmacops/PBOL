package es.polarbear.PBOL;

public class PR {
    static void print() {
        System.out.println();
    }

    static void print(String s) {
        System.out.println(s);
    }

    static void print(int s) {
        System.out.println(s);
    }

    static void print(String[] strings) {
        for (String x : strings) {
            System.out.println(x);
        }
    }
}
